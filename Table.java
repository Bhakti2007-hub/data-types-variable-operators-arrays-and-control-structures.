/*
 * File: Table.java
 * Description:
 * This program prints the multiplication table of 5 from 5 to 50
 * using a for loop. It demonstrates loop control and output formatting in Java.
 */

public class Table
{
	public static void main(String args[])
	{
		System.out.println("Table of 5:");
		for (int i = 5; i <= 50; i = i + 5)
		{
			System.out.println(i);
		}
	}
}
